//AUTHORS FUNCTION
function generateAuthors(authors){
    let tempStr = "";
    authors.forEach(function(element, index){
        tempStr += `Author ${(index+1)}:           ${element.voornaam} ${element.familienaam}
                    ${element.thumbnailUrl}

`
    })
    return tempStr;
};



function generateComments(array){
    let tempStr = "";
    array.forEach(function (element){
        tempStr += 
`
* ${element.bericht}
Written by: ${element.nickName}
`

    });
    return tempStr;
};

// POST FUNCTION
function generatePost(array){
    let seperator = "-----------------------------------------------------------------------------------------------------------";
    let seperator2 = "==========================================================================================================="; 
    let tempStr ="";
    array.forEach(function (element, index){
       tempStr += 
`

${seperator2}
Post ${(index+1)}:             ${element.titel}
${seperator2}
Synopsis:           ${element.synopsis}
${seperator}
Body:               ${element.body}                    
Thumbnail URL:      ${element.thumbnailUrl}
${seperator}
Created on:         ${element.aanmaakDatum}
Modified on:        ${element.modificatieDatum}
${seperator}
${generateAuthors(element.auteurs)}
${seperator}
Comments:
${generateComments(element.commentaren)}
`   
    });
    return tempStr;
};

